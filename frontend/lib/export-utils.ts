/**
 * Parse markdown content into docx elements (paragraphs only)
 * Tables are rendered as formatted text for compatibility
 */
function parseMarkdownToDocxElements(content: string, title: string): Paragraph[] {
  const elements: Paragraph[] = []
  
  // Title
  elements.push(
    new Paragraph({
      children: [
        new TextRun({
          text: title,
          bold: true,
          size: 56, // 28pt
          color: '1e293b',
        }),
      ],
      spacing: { after: 100 },
    })
  )
  
  // Subtitle
  elements.push(
    new Paragraph({
      children: [
        new TextRun({
          text: `Research Brief • ${new Date().toLocaleDateString()}`,
          size: 24, // 12pt
          color: '64748b',
        }),
      ],
      spacing: { after: 400 },
      border: {
        bottom: {
          color: '3b82f6',
          space: 10,
          style: BorderStyle.SINGLE,
          size: 24,
        },
      },
    })
  )
  
  // Parse content lines
  const lines = content.split('\n')
  let i = 0
  
  while (i < lines.length) {
    const line = lines[i]
    const trimmedLine = line.trim()
    
    // Check if this is the start of a table
    // Render tables as formatted text for Word (native Table causes corruption)
    if (trimmedLine.includes('|') && i + 1 < lines.length && lines[i + 1].includes('|') && lines[i + 1].includes('-')) {
      // Collect all table lines
      const tableLines: string[] = []
      while (i < lines.length && lines[i].includes('|')) {
        tableLines.push(lines[i])
        i++
      }
      
      // Parse table and render as formatted paragraphs
      const table = parseMarkdownTable(tableLines)
      if (table) {
        // Add spacing before table
        elements.push(new Paragraph({ children: [], spacing: { after: 100 } }))
        
        // Render header row as bold
        const headerText = table.headers.join('  |  ')
        elements.push(
          new Paragraph({
            children: [
              new TextRun({
                text: headerText,
                bold: true,
                size: 22,
              }),
            ],
            spacing: { after: 50 },
            shading: {
              type: ShadingType.SOLID,
              color: 'f1f5f9',
              fill: 'f1f5f9',
            },
          })
        )
        
        // Add separator line
        elements.push(
          new Paragraph({
            children: [
              new TextRun({
                text: '─'.repeat(Math.min(headerText.length, 60)),
                size: 22,
                color: 'cbd5e1',
              }),
            ],
            spacing: { after: 50 },
          })
        )
        
        // Render data rows
        for (const row of table.rows) {
          const rowText = row.join('  |  ')
          elements.push(
            new Paragraph({
              children: parseTextWithFormatting(rowText),
              spacing: { after: 50 },
            })
          )
        }
        
        // Add spacing after table
        elements.push(new Paragraph({ children: [], spacing: { after: 200 } }))
      }
      continue
    }
    
    // Empty line
    if (!trimmedLine) {
      elements.push(new Paragraph({ children: [] }))
      i++
      continue
    }
    
    // H1
    if (trimmedLine.startsWith('# ')) {
      elements.push(
        new Paragraph({
          children: [
            new TextRun({
              text: trimmedLine.replace(/^# /, ''),
              bold: true,
              size: 48, // 24pt
            }),
          ],
          heading: HeadingLevel.HEADING_1,
          spacing: { before: 400, after: 200 },
        })
      )
      i++
      continue
    }
    
    // H2
    if (trimmedLine.startsWith('## ')) {
      elements.push(
        new Paragraph({
          children: [
            new TextRun({
              text: trimmedLine.replace(/^## /, ''),
              bold: true,
              size: 36, // 18pt
            }),
          ],
          heading: HeadingLevel.HEADING_2,
          spacing: { before: 300, after: 150 },
          border: {
            bottom: {
              color: 'e2e8f0',
              space: 5,
              style: BorderStyle.SINGLE,
              size: 8,
            },
          },
        })
      )
      i++
      continue
    }
    
    // H3
    if (trimmedLine.startsWith('### ')) {
      elements.push(
        new Paragraph({
          children: [
            new TextRun({
              text: trimmedLine.replace(/^### /, ''),
              bold: true,
              size: 28, // 14pt
            }),
          ],
          heading: HeadingLevel.HEADING_3,
          spacing: { before: 200, after: 100 },
        })
      )
      i++
      continue
    }
    
    // Bullet points
    if (trimmedLine.startsWith('- ') || trimmedLine.startsWith('* ')) {
      const text = trimmedLine.replace(/^[-*] /, '')
      elements.push(
        new Paragraph({
          children: parseTextWithFormatting(text),
          bullet: { level: 0 },
          spacing: { after: 80 },
        })
      )
      i++
      continue
    }
    
    // Numbered lists
    if (/^\d+\. /.test(trimmedLine)) {
      const text = trimmedLine.replace(/^\d+\. /, '')
      elements.push(
        new Paragraph({
          children: parseTextWithFormatting(text),
          numbering: { reference: 'default-numbering', level: 0 },
          spacing: { after: 80 },
        })
      )
      i++
      continue
    }
    
    // Regular paragraph
    elements.push(
      new Paragraph({
        children: parseTextWithFormatting(trimmedLine),
        spacing: { after: 150 },
      })
    )
    i++
  }
  
  // Footer
  elements.push(
    new Paragraph({
      children: [
        new TextRun({
          text: `Generated by DealMotion • ${new Date().toLocaleDateString()}`,
          size: 20, // 10pt
          color: '94a3b8',
          italics: true,
        }),
      ],
      alignment: AlignmentType.CENTER,
      spacing: { before: 600 },
      border: {
        top: {
          color: 'e2e8f0',
          space: 10,
          style: BorderStyle.SINGLE,
          size: 8,
        },
      },
    })
  )
  
  return elements
}

/**
 * Parse text with bold/italic formatting into TextRuns
 */
function parseTextWithFormatting(text: string): TextRun[] {
  const runs: TextRun[] = []
  
  // Simple regex-based parsing for bold and italic
  const parts = text.split(/(\*\*.*?\*\*|\*.*?\*)/g)
  
  for (const part of parts) {
    if (!part) continue
    
    if (part.startsWith('**') && part.endsWith('**')) {
      runs.push(
        new TextRun({
          text: part.slice(2, -2),
          bold: true,
          size: 24,
        })
      )
    } else if (part.startsWith('*') && part.endsWith('*')) {
      runs.push(
        new TextRun({
          text: part.slice(1, -1),
          italics: true,
          size: 24,
        })
      )
    } else {
      runs.push(
        new TextRun({
          text: part,
          size: 24,
        })
      )
    }
  }
  
  return runs.length > 0 ? runs : [new TextRun({ text, size: 24 })]
}
